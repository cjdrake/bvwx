    ("3b000", "2b11"),
    ("3b001", "2b10"),
    ("3b010", "2b01"),
    ("3b011", "2b01"),
    ("3b100", "2b00"),
    ("3b101", "2b00"),
    ("3b110", "2b00"),
    ("3b111", "2b00"),
